/**
 * Update the chart. This happens either on updating (moving) the range input,
 * or from a timer when the timeline is playing.
 */
function update(increment) {
    var input = document.querySelector('#play-range'),
        output = document.querySelector('#play-output');

    if (increment) {
        input.value = parseInt(input.value, 10) + increment;
    }
    chart.series[1].setData(dataSequence[input.value].data); // Increment dataset (updates chart)
    output.innerHTML = dataSequence[input.value].name + ' km/h'; // Output value
    accidentsNumber.innerHTML = dataSequence[input.value].data.length + ' accidents'; // # of accidents
    if (input.value >= input.max) { // Auto-pause
        pause(playBtn);
    }
}

/**
 * Play the timeline.
 */
function play(button) {
    button.title = 'pause';
    button.className = 'fa fa-pause';
    chart.sequenceTimer = setInterval(function () {
        update(1);
    }, 800);

}

/**
 * Pause the timeline, either when the range is ended, or when clicking the pause button.
 * Pausing stops the timer and resets the button to play mode.
 */
function pause(button) {
    button.title = 'play';
    button.className = 'fa fa-play';
    clearTimeout(chart.sequenceTimer);
    chart.sequenceTimer = undefined;
}

/**
 * Toggle play and pause from the button
 */
playBtn.addEventListener('click', function () {
    if (chart.sequenceTimer === undefined) {
        play(this);
    } else {
        pause(this);
    }
});

/**
 * Update the chart when the input is changed
 */
document.querySelector('#play-range').addEventListener('input', function () {
    update();
});
