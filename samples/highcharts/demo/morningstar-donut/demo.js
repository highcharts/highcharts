// Parse the dataset to Highcharts accepted format utilizing series.key property
const data = Performance.underlyHoldings
    .map(holding => [holding.holdingId, holding.weight, holding.marketValue]);

Highcharts.chart('container', {
    title: {
        text: 'Assets allocation',
        align: 'center'
    },
    subtitle: {
        text: '',
        style: {
            fontSize: 20
        },
        floating: true,
        verticalAlign: 'middle',
        y: 30
    },

    legend: {
        enabled: false
    },

    tooltip: {
        format: 'Market Value of <b>{key}</b>: {point.marketValue}'
    },

    plotOptions: {
        series: {
            borderWidth: 0,
            colorByPoint: true,
            type: 'pie',
            size: '100%',
            innerSize: '80%',
            dataLabels: {
                enabled: true,
                distance: '10%',
                style: {
                    fontWeight: 'bold',
                    fontSize: '13px'
                },
                connectorWidth: 1,
                formatter() {
                    return `${this.key} (${this.percentage.toFixed(2)}%)`;
                }
            }
        }
    },
    colors: [
        '#FCE700', '#F8C4B4', '#F6E1EA', '#B8E8FC', '#BCE29E',
        '#F9A1BC', '#FDD7AA', '#D0F4DE', '#C4FCEF', '#FFDCDC',
        '#D6E3FF', '#E6FFB3', '#F3EACF', '#FBE4F6', '#C2E7D9'
    ],
    series: [
        {
            keys: ['name', 'y', 'marketValue'],
            type: 'pie',
            data: data,
            point: {
                events: {
                    mouseOver() {
                        const chart = this.series.chart;
                        const { name, marketValue, percentage } = this;
                        chart.update({
                            subtitle: {
                                text: `
                                    <b>${name}</b> (${percentage.toFixed(2)}%)
                                    <br>
                                    Market value: ${marketValue}
                                `
                            }
                        });
                    },
                    mouseOut() {
                        const chart = this.series.chart;
                        chart.update({
                            subtitle: {
                                text: ''
                            }
                        });
                    }
                }
            }
        }
    ]
});
