function drawChart(dataset) {
    Highcharts.chart('container', {
        chart: {
            type: 'area'
        },
        title: {
            text: 'Import, Export of goods in Norway (NOK million)'
        },
        subtitle: {
            text: 'Source: <a ' +
                'href="https://www.ssb.no/en/statbank/table/08792"' +
                ' target="_blank">SSB</a>'
        },
        xAxis: {
            categories: dataset.categories,
            lineWidth: 0
        },
        yAxis: {
            title: {
                text: 'MNOK'
            },
            plotLines: [{
                color: 'var(--highcharts-neutral-color-100, black)',
                value: 0,
                width: 2
            }]
        },
        plotOptions: {
            series: {
                marker: {
                    enabled: false
                }
            }
        },
        credits: {
            enabled: false
        },
        series: [{
            name: 'Exports',
            data: dataset.exports
        }, {
            name: 'Imports',
            data: dataset.imports.map(point => ({
                y: -point
            }))
        }, {
            name: 'Trade Balance',
            data: dataset.imports.map((point, i) => ({
                y: dataset.exports[i] - point
            }))
        }]
    });
}

