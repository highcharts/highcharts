(async () => {

    const topology = await fetch(
        'https://code.highcharts.com/mapdata/countries/gh/gh-all.topo.json'
    ).then(response => response.json());

    // Prepare demo data. The data is joined to map using value of 'hc-key'
    // property by default. See API docs for 'joinBy' for more info on linking
    // data and map.
    const data = [
        ['gh-ah', 10], ['gh-ep', 11], ['gh-wp', 12], ['gh-aa', 13],
        ['gh-tv', 14], ['gh-np', 15], ['gh-ue', 16], ['gh-uw', 17],
        ['gh-ba', 18], ['gh-cp', 19]
    ];

    // Create the chart
    Highcharts.mapChart('container', {
        chart: {
            map: topology
        },

        title: {
            text: 'Highcharts Maps basic demo'
        },

        subtitle: {
            text: 'Source map: <a href="https://code.highcharts.com/mapdata/countries/gh/gh-all.topo.json">Ghana</a>'
        },

        mapNavigation: {
            enabled: true,
            buttonOptions: {
                verticalAlign: 'bottom'
            }
        },

        colorAxis: {
            min: 0
        },

        series: [{
            data: data,
            name: 'Random data',
            states: {
                hover: {
                    color: '#BADA55'
                }
            },
            dataLabels: {
                enabled: true,
                format: '{point.name}'
            }
        }]
    });

})();
